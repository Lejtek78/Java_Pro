package unit_testing.junit5;

public class Calculator {

	public int integerDivision(int dividend, int divisor) {
		return dividend / divisor;
	}
	
	public int integerSubtraction(int minuend, int subtrahend) {
		return minuend - subtrahend;
	}
	
	public int integerMultiplication(int factor1, int factor2) {
		return factor1 * factor2;
	}
	
	public int integerAddition(int summand1, int summand2) {
		return summand1 + summand2;
	}
	
}
